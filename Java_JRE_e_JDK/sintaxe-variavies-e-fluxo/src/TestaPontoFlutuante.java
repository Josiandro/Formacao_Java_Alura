
public class TestaPontoFlutuante {
	public static void main(String[] args) {
		
		double salario;
		salario = 1250.70;
		System.out.println("Meu salário é R$ " + salario);
		
		double idade = 37;
		System.out.println("Idade inteira na variável double: " + idade);
		
		double divisao = 3.14 / 2;
		System.out.println("Resultado da divisão: " + divisao);
		
		int outraDivisao = 5 / 2;
		System.out.println("Divisão de números inteiros: " + outraDivisao);
		
		double novaTentativa = 5 / 2;
		System.out.println("1 - Nova tentativa com variável double: " + novaTentativa);
		
		double novaTentativa2 = 5.0 / 2;
		System.out.println("2 - Nova tentativa com variável double: " + novaTentativa2);
	}
}
